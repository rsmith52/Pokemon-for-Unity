    [CreateAssetMenu(fileName = "Item", menuName = "Pokemon/Item")]
    public class Item : ScriptableObject
    {
        #region Fields

        public static Dictionary<Items, Item> items;

        #endregion


        #region Scriptable Object

        public uint item_id;
        public string plural_name;
        public BagPockets bag_pocket;
        public int price;
        public string description;
        public OutOfBattleUses out_of_battle_use;
        public InBattleUses in_battle_use;
        public SpecialItemTypes special_item_type;
        public Moves move_taught;

        #endregion


        #region Static Methods

        public static Sprite GetItemIcon(uint item_id)
        {
            string item_icon_path = Settings.ITEM_ICON_PATH;

            string formatted_number = item_id.ToString().PadLeft(3, '0');

            string file_name = Settings.ITEM_ICON_PREFIX + formatted_number;
            string local_path = Path.GetFileName(item_icon_path);
            string file_path = Path.Combine(local_path, file_name);

            try
            {
                return Resources.Load<Sprite>(file_path);
            }
            catch
            {
                file_path = Path.Combine(local_path, Settings.ITEM_MISSING_ICON);
                return Resources.Load<Sprite>(file_path);
            }
        }

        #endregion

    }
